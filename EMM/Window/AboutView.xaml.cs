namespace EMM
{
    /// <summary>
    /// Interaction logic for AboutView.xaml
    /// </summary>
    public partial class AboutView : Window
    {
        public AboutView()
        {
            InitializeComponent();
            this.DataContext = this;
        }

        public string CurrentVersion => string.Format("Current Version: {0}", Assembly.GetExecutingAssembly().GetName().Version.ToString(3));

        private void Site_RequestNavigate(object sender, System.Windows.Navigation.RequestNavigateEventArgs e)
        {
            Process.Start(new ProcessStartInfo(e.Uri.AbsoluteUri));
            e.Handled = true;
        }
    }
